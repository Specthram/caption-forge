"""Repository for the SQLite storage mode.

All reads and writes against the datasets database go through this module: it
turns the raw schema (see :mod:`src.db`) into dataset/media/caption operations.
The captioning revision model is the heart of it:

* a *caption* is the identity ``(media, caption_type)``; its text lives in an
  append-only chain of *revisions* (``parent_revision_id`` links each to the
  one it was derived from);
* ``caption.head_revision_id`` is the mutable "head" of that chain — the
  canonical, type-wide caption;
* a *dataset* references a caption in one of two modes:
  ``follow`` (always shows the head) or ``pinned`` (shows a frozen revision),
  recorded in ``dataset_caption``.

So a type-wide edit advances the head (every ``follow`` dataset sees it), while
a dataset-scoped edit branches a new revision and pins only that dataset — the
other datasets, still following the head, never see it.

Each function opens its own short-lived connection (the safe pattern under
Gradio's worker threads); multi-statement writes run inside one transaction.
"""

